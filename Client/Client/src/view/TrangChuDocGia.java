    private void timSach() {
        taiLieus.clear();
        clientCtr.moSocket();
        clientCtr.guiyeucau("oke");
        taiLieus = clientCtr.nhandanhsachtailieu();
        if (rbtimtheoten.isSelected()) {
            if (txttimtheoten.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn"
                        + " tên tài liệu cần tìm kiếm");

            } else {
                var s1 = dataControllerImp.timtailieutheoten(taiLieus,
                        txttimtheoten.getText());
                if (s1.size() > 0) {
                    JOptionPane.showMessageDialog(rootPane,
                            "Tìm thấy " + s1.size() + " kết quả");
                    tableModeltailieu.setRowCount(0);
                    for (TaiLieu s : s1) {
                        showSach(s);
                    }
                } else {
                    taiLieus.clear();
                    showtaiLieus();
                    JOptionPane.showMessageDialog(rootPane,
                            "Không tìm thấy kết quả nào");
                }

            }
        } else if (rbtimsachtheoma.isSelected()) {
            if (!txttimsachtheoma.getText().isEmpty()) {

                var s2 = dataControllerImp.
                        timtailieutheoma(taiLieus, txttimsachtheoma.getText());
                if (s2.size() > 0) {
                    tableModeltailieu.setRowCount(0);
                    for (TaiLieu s : s2) {
                        showSach(s);
                    }
                    JOptionPane.showMessageDialog(rootPane,
                            "Tìm thấy " + s2.size() + " kết quả");
                } else {
                    taiLieus.clear();
                    showtaiLieus();
                    JOptionPane.showMessageDialog(rootPane,
                            "Không tìm thấy kết quả nào");
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập"
                        + " mã tài liệu cần tìm kiếm");
            }
        } else if (rbtimtheodanhmuc.isSelected()) {
            if (combodanhmuc.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn"
                        + " danh mục tài liệu cần tìm kiếm");
            } else {
                var major1 = combodanhmuc.getSelectedItem().toString();
                var s3 = dataControllerImp.timtailieutheodanhmuc1(taiLieus, major1);
                if (s3.size() > 0) {
                    taiLieus.clear();
                    taiLieus.addAll(s3);
                    showtaiLieus();
                    JOptionPane.showMessageDialog(rootPane,
                            "Tìm thấy " + s3.size() + " kết quả");
                } else {
                    taiLieus.clear();
                    showtaiLieus();
                    JOptionPane.showMessageDialog(rootPane,
                            "Không tìm thấy kết quả nào");
                }
            }
        } else if (rbtimtheonxb.isSelected()) {
            if (combonxb.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(rootPane,
                        "Vui lòng chọn một nhà xuất bản cần tìm kiếm");
            } else {
                var major1 = combonxb.getSelectedItem().toString();
                var s3 = dataControllerImp.timtailieutheonxb1(taiLieus, major1);
                if (s3.size() > 0) {
                    taiLieus.clear();
                    taiLieus.addAll(s3);
                    showtaiLieus();
                    JOptionPane.showMessageDialog(rootPane,
                            "Tìm thấy " + s3.size() + " kết quả");
                } else {
                    taiLieus.clear();
                    showtaiLieus();
                    JOptionPane.showMessageDialog(rootPane,
                            "Không tìm thấy kết quả nào");
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn"
                    + " cách thức tìm kiếm");
        }
        clientCtr.dongketnoi();
    }

