    public TrangChuDocGia(int madocgia) {
        this();
        this.madocgia = madocgia;
        for (PhieuMuon s : phieuMuons) {
            if (s.getMaBanDoc() == madocgia && !(s.getTrangThai().trim().equals("Đã trả"))) {
                showdata(s);
                break;
            }
        }
        for (DocGia banDoc : docGias) {
            if (banDoc.getMaDocGia() == madocgia) {
                docGia1 = banDoc;
                showdata1(banDoc);
                break;
            }
        }
    }

